#include<bits/stdc++.h>
using namespace std ;
using ll = long long int ;

void solve(ll m , ll n)
{
    ll sum = m % 10 + n % 10 ;

    cout << sum << endl ;
}

int main()
{
    ll m , n ;  cin >> m >> n ;

    solve(m , n) ;
    return 0 ;
}

// Time Complexity:  O(1)
// Space Complexity: O(1)
